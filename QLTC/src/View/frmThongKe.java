    void loadNV() {
        ArrayList<TblPhieuGui> list;
        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd", Locale.getDefault());
            String tungay = dateFormat.format(datetungay.getDate());
            String denngay = dateFormat.format(datedenngay.getDate());
            list = pgDL.getThongKe(tungay, denngay);
            table = (DefaultTableModel) tblPhieuGui.getModel();
            double tongtien = 0;
            for (TblPhieuGui nv : list) {
                table.addRow(new Object[]{nv.getMaPhieuGui(), nv.getTenKH(), nv.getNgayGui(), nv.getLoai(), nv.getTrangThai(), nv.getTongTien()});
                tongtien += nv.getTongTien();
            }
            lbldoanhthu.setText(tongtien + "vnđ");
        } catch (SQLException ex) {

        }
    }
