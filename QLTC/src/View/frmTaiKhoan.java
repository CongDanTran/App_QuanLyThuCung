    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (them) {
            if (KTDL()) {
                // String manv=cboMaNhanVien.getSelectedItem().toString();
                TblNhanVien nv = (TblNhanVien) cboMaNhanVien.getSelectedItem();
                String manv = String.valueOf(nv.getMaNV());
                try {
                    Connection conn = dao.getConnect();
                    String caulenh = "INSERT INTO tbl_taikhoan VALUES('" + manv + "','" + txtTenTaiKhoan.getText() + "','" + txtMatKhau.getText() + "',N'" + txtQuyen.getText() + "')";
                    boolean a = dao.Lenh(caulenh, conn);
                    if (a) {
                        tblTaiKhoan.removeAll();
                        table.setRowCount(0);
                        loadNV();
                        JOptionPane.showMessageDialog(this, "Thêm thành công");
                    } else {
                        JOptionPane.showMessageDialog(this, "Thêm KHÔNG thành công");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
        if (sua) {
                TblNhanVien nv = (TblNhanVien) cboMaNhanVien.getSelectedItem();
            String manv = String.valueOf(nv.getMaNV());

                try {
                    Connection conn = dao.getConnect();
                    String caulenh = "UPDATE tbl_taikhoan SET MatKhau='" + txtMatKhau.getText() + "',Quyen=N'" + txtQuyen.getText() + "'where (MaNV=" + manv + " and TenTK='" + txtTenTaiKhoan.getText() + "')";
                    boolean a = dao.Lenh(caulenh, conn);
                    if (a) {
                        tblTaiKhoan.removeAll();
                        table.setRowCount(0);
                        loadNV();
                        JOptionPane.showMessageDialog(this, "Sửa thành công");
                    } else {
                        JOptionPane.showMessageDialog(this, "Sửa KHÔNG thành công");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
                }
            
        }
        if (xoa) {
            TblNhanVien nv = (TblNhanVien) cboMaNhanVien.getSelectedItem();
            String manv = String.valueOf(nv.getMaNV());
            try {
                Connection conn = dao.getConnect();
                String caulenh = "DELETE FROM tbl_taikhoan WHERE (MaNV=" + manv + " and TenTK='" + txtTenTaiKhoan.getText() + "')";
                boolean a = dao.Lenh(caulenh, conn);
                if (a) {
                    tblTaiKhoan.removeAll();
                    table.setRowCount(0);
                    loadNV();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa KHÔNG thành công");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed
    void hienthitxt(int i) {
        TableModel model = tblTaiKhoan.getModel();
        String cbosub = model.getValueAt(i, 0).toString();
        for (int j = 0; j < cboMaNhanVien.getItemCount(); j++) {
            if (cboMaNhanVien.getItemAt(j).toString().equalsIgnoreCase(cbosub)) {
                cboMaNhanVien.setSelectedIndex(j);
            }
        }
        txtTenTaiKhoan.setText(model.getValueAt(i, 1).toString());
        txtMatKhau.setText(model.getValueAt(i, 2).toString());
        txtQuyen.setText(model.getValueAt(i, 3).toString());

    }
