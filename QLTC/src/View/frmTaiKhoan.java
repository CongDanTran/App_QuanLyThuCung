    private boolean KTDL() throws HeadlessException {
        StringBuilder sb = new StringBuilder();
        if (txtTenTaiKhoan.getText().equals("")) {
            sb.append("Vui lòng nhập tên tài khoản\n");
            txtTenTaiKhoan.requestFocus();
        }
        if (txtMatKhau.getText().equals("")) {
            sb.append("Vui lòng nhập mật khẩu\n");
            txtMatKhau.requestFocus();
        }
        if (txtQuyen.getText().equals("")) {
            sb.append("Vui lòng nhập quyền\n");
            txtQuyen.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }
