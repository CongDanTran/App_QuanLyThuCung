    private boolean KTDL() throws HeadlessException {
        StringBuilder sb = new StringBuilder();
        if (txtTenDichVu.getText().equals("")) {
            sb.append("Vui lòng nhập tên dịch vụ\n");
            txtTenDichVu.requestFocus();
        }
        if (txtGia.getText().equals("")) {
            sb.append("Vui lòng nhập giá");
            txtGia.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

