    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (them) {
            if (KTDL()) {
                float gia = Float.parseFloat(txtGia.getText());
                try {
                    Connection conn = dao.getConnect();
                    String caulenh = "INSERT INTO tbl_DichVu VALUES(N'" + txtTenDichVu.getText() + "','" + gia + "')";
                    boolean a = dao.Lenh(caulenh, conn);
                    if (a) {
                        tblDichVu.removeAll();
                        table.setRowCount(0);
                        loadNV();
                        JOptionPane.showMessageDialog(this, "Thêm thành công");
                    } else {
                        JOptionPane.showMessageDialog(this, "Thêm KHÔNG thành công");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmDichVu.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        if (sua) {
            float gia = Float.parseFloat(txtGia.getText());
            try {
                Connection conn = dao.getConnect();
                String caulenh = "UPDATE tbl_DichVu SET TenDichVu=N'" + txtTenDichVu.getText() + "',Gia='" + gia + "'where MaDV=" + txtMaDichVu.getText();
                boolean a = dao.Lenh(caulenh, conn);
                if (a) {
                    tblDichVu.removeAll();
                    table.setRowCount(0);
                    loadNV();
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa KHÔNG thành công");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmDichVu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (xoa) {
            try {
                Connection conn = dao.getConnect();
                String caulenh = "DELETE FROM tbl_DichVu WHERE MaDV=" + txtMaDichVu.getText();
                boolean a = dao.Lenh(caulenh, conn);
                if (a) {
                    tblDichVu.removeAll();
                    table.setRowCount(0);
                    loadNV();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa KHÔNG thành công");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmDichVu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

